export type NativeMessageKey = keyof (typeof catalogs)["en"];

export function normalizeNativeLocale(value: string | null | undefined): NativeLocale {
  return value === "zh-CN" || value === "zh" ? "zh-CN" : "en";
}

export function t(
  locale: NativeLocale,
  key: NativeMessageKey,
  values: Record<string, string | number> = {},
): string {
  const template = catalogs[locale][key];
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export async function loadNativeLocale(store: SettingsStore): Promise<NativeLocale> {
  return normalizeNativeLocale(await store.getItem(nativeLocaleStorageKey));
}

export async function saveNativeLocale(store: SettingsStore, locale: NativeLocale): Promise<void> {
  await store.setItem(nativeLocaleStorageKey, locale);
}

export function nativeApiErrorMessage(locale: NativeLocale, code: string): string {
  const keyByCode: Record<string, NativeMessageKey> = {
    API_NETWORK_ERROR: "apiUnreachable",
    FORBIDDEN: "permissionDenied",
    INVALID_CREDENTIALS: "invalidCredentials",
    SYSTEM_USER_LOGIN_NOT_ALLOWED: "systemUser",
    UNAUTHENTICATED: "sessionExpired",
    AUTH_REFRESH_EXPIRED: "sessionExpired",
    AUTH_REFRESH_REPLAYED: "sessionRevoked",
    AUTH_SESSION_REVOKED: "sessionRevoked",
    USER_INACTIVE: "inactiveUser",
  };
  return t(locale, keyByCode[code] ?? "genericError");
}

export function assertNativeCatalogParity(): void {
  const englishKeys = Object.keys(catalogs.en).sort();
  const chineseKeys = Object.keys(catalogs["zh-CN"]).sort();
  if (englishKeys.join("|") !== chineseKeys.join("|")) {
    throw new Error("Native locale catalogs must expose identical keys.");
  }
}
